/*
Program to demonstrate the use of a for loop. It will display a set of numbers 1 - 10
*/

#include <stdio.h>

int main()
{
 	int i;

 	// Display the numbers 1 - 10
 	for(i = 1; i < 11; i++)
 	{
   		// Display the contents of the variable i
   		printf("%d\n", i);
 	}

 	return 0;

} // end main ()